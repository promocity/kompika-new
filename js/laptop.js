var laptop_nav1 = document.querySelector(".laptop-content-navigation-display");
    var dot_active1 = document.querySelector(".dot-active-laptop-1");
    var laptop_hover1 = document.querySelector(".laptop-hover-display + img");



    var laptop_hover_div1 = document.querySelector(".laptop-hover-display");
    var nav_before1 = document.querySelector(".laptop-content-navigation-display .nav-before");
    var nav_after1 = document.querySelector(".laptop-content-navigation-display .nav-after");

    laptop_nav1.addEventListener("mouseover", function(){
      dot_active1.classList.add("dot-active-show");
      laptop_hover1.classList.add("dot-active-show");
    });


    laptop_nav1.addEventListener("mouseout", function(){
      dot_active1.classList.remove("dot-active-show");
      laptop_hover1.classList.remove("dot-active-show");
    });






    laptop_hover_div1.addEventListener("mouseover", function(){
      nav_after1.classList.add("line-hover");
      dot_active1.classList.add("dot-active-show");
      laptop_nav1.classList.add("text-hover");
    });


    laptop_hover_div1.addEventListener("mouseout", function(){
      nav_after1.classList.remove("line-hover");
      dot_active1.classList.remove("dot-active-show");
      laptop_nav1.classList.remove("text-hover");
    });














