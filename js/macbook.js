    var macbook_nav1 = document.querySelector(".mac-book-content-navigation-display");
    var dot_active1 = document.querySelector(".dot-active-mac-book-1");
    var macbook_hover1 = document.querySelector(".mac-book-hover-display + img");



    var macbook_hover_div1 = document.querySelector(".mac-book-hover-display");
    var nav_after1 = document.querySelector(".mac-book-content-navigation-display .nav-after");

    macbook_nav1.addEventListener("mouseover", function(){
      dot_active1.classList.add("dot-active-show");
      macbook_hover1.classList.add("dot-active-show");
    });


    macbook_nav1.addEventListener("mouseout", function(){
      dot_active1.classList.remove("dot-active-show");
      macbook_hover1.classList.remove("dot-active-show");
    });






    macbook_hover_div1.addEventListener("mouseover", function(){
      nav_after1.classList.add("line-hover");
      dot_active1.classList.add("dot-active-show");
      macbook_nav1.classList.add("text-hover");
    });


    macbook_hover_div1.addEventListener("mouseout", function(){
      nav_after1.classList.remove("line-hover");
      dot_active1.classList.remove("dot-active-show");
      macbook_nav1.classList.remove("text-hover");
    });











