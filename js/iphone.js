
                            /*1*/

    var iphone_nav1 = document.querySelector(".iphone-content-navigation-camera");
    var dot_active1 = document.querySelector(".dot-active-1");
    var iphone_hover1 = document.querySelector(".iphone-hover-camera + img");



    var iphone_hover_div1 = document.querySelector(".iphone-hover-camera");
    var nav_before1 = document.querySelector(".iphone-content-navigation-camera .nav-before");
    var nav_after1 = document.querySelector(".iphone-content-navigation-camera .nav-after");

    iphone_nav1.addEventListener("mouseover", function(){
      dot_active1.classList.add("dot-active-show");
      iphone_hover1.classList.add("dot-active-show");
    });


    iphone_nav1.addEventListener("mouseout", function(){
      dot_active1.classList.remove("dot-active-show");
      iphone_hover1.classList.remove("dot-active-show");
    });






    iphone_hover_div1.addEventListener("mouseover", function(){
      nav_before1.classList.add("line-hover");
      nav_after1.classList.add("line-hover");
      dot_active1.classList.add("dot-active-show");
      iphone_nav1.classList.add("text-hover");
    });


    iphone_hover_div1.addEventListener("mouseout", function(){
      nav_before1.classList.remove("line-hover");
      nav_after1.classList.remove("line-hover");
      dot_active1.classList.remove("dot-active-show");
      iphone_nav1.classList.remove("text-hover");
    });




