    var imac_nav1 = document.querySelector(".imac-content-navigation-display");
    var dot_active1 = document.querySelector(".dot-active-imac-1");
    var imac_hover1 = document.querySelector(".imac-hover-display + img");



    var imac_hover_div1 = document.querySelector(".imac-hover-display");
    var nav_after1 = document.querySelector(".imac-content-navigation-display .nav-after");

    imac_nav1.addEventListener("mouseover", function(){
      dot_active1.classList.add("dot-active-show");
      imac_hover1.classList.add("dot-active-show");
    });


    imac_nav1.addEventListener("mouseout", function(){
      dot_active1.classList.remove("dot-active-show");
      imac_hover1.classList.remove("dot-active-show");
    });






    imac_hover_div1.addEventListener("mouseover", function(){
      nav_after1.classList.add("line-hover");
      dot_active1.classList.add("dot-active-show");
      imac_nav1.classList.add("text-hover");
    });


    imac_hover_div1.addEventListener("mouseout", function(){
      nav_after1.classList.remove("line-hover");
      dot_active1.classList.remove("dot-active-show");
      imac_nav1.classList.remove("text-hover");
    });










